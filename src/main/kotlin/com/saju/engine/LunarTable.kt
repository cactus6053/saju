    private fun info(year: Int): Int {
        require(year in MIN_YEAR..MAX_YEAR) { "지원 범위(1900~2100)를 벗어난 연도입니다: $year" }
        return INFO[year - MIN_YEAR]
    }

    // 윤달 월 번호 (0 = 윤달 없음)
    fun leapMonth(year: Int): Int = info(year) and 0xf

    // 일반 월(1~12)의 일수
    fun monthDays(year: Int, month: Int): Int {
        require(month in 1..12) { "월은 1~12 범위여야 합니다: $month" }
        return if (info(year) and (0x8000 shr (month - 1)) != 0) 30 else 29
    }

    // 윤달의 일수 (윤달 없으면 0)
    fun leapMonthDays(year: Int): Int = when {
        leapMonth(year) == 0 -> 0
        info(year) and 0x10000 != 0 -> 30
        else -> 29
    }

    // 해당 음력 연도의 총 일수
    fun yearDays(year: Int): Int =
        (1..12).sumOf { monthDays(year, it) } + leapMonthDays(year)
}
