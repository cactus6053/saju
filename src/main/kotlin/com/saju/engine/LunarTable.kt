// 1900~2100년 음력 데이터 비트마스크 테이블.
// 인코딩(연도당 1개 정수):
//   bit 0-3   : 윤달 월 번호 (0 = 윤달 없음)
//   bit 4-15  : 12월~1월의 대소 (bit 15 = 1월, 1 = 큰달 30일, 0 = 작은달 29일)
